symbols_values = {
    '#': 5,
    'O': 3,
    'X': 1,
    '!': -1,
    '!!': -3,
    '!!!': -5
}

def check_score(s):
    sum_total = 0
    for row in s:
        sum = 0
        for value in row:
            sum += symbols_values[value]
        if sum > 0:
            sum_total += sum
    return sum_total
        


